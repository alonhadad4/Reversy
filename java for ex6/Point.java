public class Point {
	private int x;
	private int y;

	public Point(int firstNum , int secondNum) {
		this.x = firstNum;
		this.y = secondNum;
	}

	public void setX(int num) {
		this.x = num;
	}

	public void setY(int num) {
		this.y = num;
	}

	public int getX() {
		return this.x;
	}

	public int getY() {
		return this.y;
	}
}